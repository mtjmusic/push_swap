#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	out;

	if (n == INT_MIN)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	out = n % 10 + '0';
	ft_putchar_fd(out, fd);
}

